const removeGoogleFont = () => {
  if (typeof document !== 'undefined') {
    const head = document.getElementsByTagName('head')[0];

    // Save the original method
    const insertBefore = head.insertBefore;

    // Replace it!
    head.insertBefore = function (newElement: any, referenceElement: any) {
      if (
        newElement.href &&
        newElement.href.indexOf('https://fonts.googleapis.com/css?family') === 0
      ) {
        return;
      }

      insertBefore.call(head, newElement, referenceElement);
    } as any;
  }
};

const render = (status: Status) => {
  return <p>{status}</p>;
};

export const GoogleMap: FC = () => {
  const prefersDarkMode = useMediaQuery('(prefers-color-scheme: dark)');
  const [center, setCenter] = useState<google.maps.LatLngLiteral>(initialCenter);
  const [notFound, setNotFound] = useState(false);

  const handleClose = (event?: React.SyntheticEvent | Event, reason?: string) => {
    if (reason === 'clickaway') {
      return;
    }
    setNotFound(false);
  };

  removeGoogleFont();
  return (
    <div className="flex h-full max-h-72">
      <Wrapper
        apiKey={'AIzaSyAet8Mk1nPvOn_AebLE5ZxXoGejOD8tPzA'}
        libraries={['places']}
        render={render}
      >
        <Map
          center={center}
          zoom={17}
          style={{ flexGrow: '1', height: '100%', minHeight: '70vh' }}
          styles={prefersDarkMode ? darkTheme : theme}
        >
          <Marker position={center} />
        </Map>
        <GooglePlaces setCenter={setCenter} setNotFound={setNotFound} />
      </Wrapper>
      <Snackbar open={notFound} autoHideDuration={6000} onClose={handleClose}>
        <Alert onClose={handleClose} severity="error" sx={{ width: '100%' }}>
          No details available for inputted data!
        </Alert>
      </Snackbar>
    </div>
  );
};

interface MapProps extends google.maps.MapOptions {
  style: { [key: string]: string };
  children?: any;
}

const Map: FC<MapProps> = ({ children, style, ...options }) => {
  const ref = useRef<HTMLDivElement>(null);
  const [map, setMap] = useState<google.maps.Map>();

  useEffect(() => {
    if (ref.current && !map) {
      setMap(new window.google.maps.Map(ref.current, {}));
    }
  }, [ref, map]);

  useEffect(() => {
    if (map) {
      map.setOptions(options);
    }
  }, [map, options]);

  return (
    <>
      <div ref={ref} style={style} />
      {cloneElement(children, { map })}
    </>
  );
};

const Marker: FC<google.maps.MarkerOptions> = (options) => {
  const [marker, setMarker] = useState<google.maps.Marker>();

  useEffect(() => {
    if (!marker) {
      setMarker(new google.maps.Marker());
    }

    // remove marker from map on unmount
    return () => {
      if (marker) {
        marker.setMap(null);
      }
    };
  }, [marker]);

  useEffect(() => {
    if (marker) {
      marker.setOptions(options);
    }
  }, [marker, options]);

  return null;
};
